import React from "react";

interface ShinyTextProps {
  text: string;
  disabled?: boolean;
  speed?: number;
  className?: string;
}

const ShinyText: React.FC<ShinyTextProps> = ({
  text,
  disabled = false,
  speed = 5,
  className = "",
}) => {
  const animationDuration = `${speed}s`;

  return (
    <div
      className={`bg-clip-text text-transparent inline-block relative ${
        disabled ? "" : "animate-shine"
      } ${className}`}
      style={{
        backgroundImage: "linear-gradient(90deg, #40ffaa, #4079ff, #40ffaa)", // warna biru-cyan mewah
        backgroundSize: "200% 100%",
        WebkitBackgroundClip: "text",
        animationDuration: animationDuration,
      }}
    >
      {text}
    </div>
  );
};

export default ShinyText;
